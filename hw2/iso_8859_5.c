bool iso_8859_5_to_unicode(uint8_t *in, uint32_t *out) {
      if (*in < 0x7F) {
        *out = *in;
        return true;
      }

      int index = *in - iso_8859_5[0];
      if (index >= sizeof(iso_8859_5_unicode)) {
        return false;
      }
      *out = iso_8859_5_unicode[index];

      return true;
    };
